-- TABELA DE MATERIAS PRIMAS QUE SAO NESCESSARIAS PARA NOSSO CONCRETO.SELECT.
CREATE TABLE MATERIA_PRIMA (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR (200) NOT NULL
)
--TABELA DE FORNECEDORES DE MATERIAS PRIMAS.
CREATE TABLE FORNECEDORES (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR (100) NOT NULL
);
-- TABELA RELACIONATIVA DE N:N (FORNECEDOR X MATERIA PRIMA FORNECIDA).
CREATE TABLE FORNECE (
    ID_FORNECEDOR NUMBER NOT NULL REFERENCES FORNECEDORES (ID),
    ID_MATERIA NUMBER NOT NULL REFERENCES MATERIA_PRIMA(ID),
    PRIMARY KEY (ID_FORNECEDOR, ID_MATERIA_PRIMA)
)
-- RECEITAS PARA TIPO DE CONCRETO.
CREATE TABLE RECEITAS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    TIPO_DE_CONCRETO VARCHAR (100)NOT NULL,
    DESCRICAO_CONCRETO VARCHAR(200) NOT NULL
)
--TABELA DE ASSOCIACAO MATERIA PRIMA PARA TIPO DE CONCRETO, TABELA DE ORGANIZACAO DA CRIACAO.
CREATE TABLE CONCRETO (
    ID_MATERIA_USADA NUMBER NOT NULL REFERENCES MATERIA_PRIMA (ID),
    ID_RECEITA NUMBER NOT NULL REFERENCES RECEITAS(ID)
)





