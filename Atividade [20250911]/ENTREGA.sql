RESPOSTA DAS QUESTOES: 

1- Por que a normalização foi essencial para a integridade dos dados da fábrica?

A normalização teve importância para prevenir os erros na geração desse projeto de banco de dados, me auxiliando a entender qual a necessidade que cada projeto precisava, manter os recursos necessários para cada entidade.

2- Em que situações a desnormalização se torna uma escolha estratégica, e não um 
erro?

Creio que a desnormalização se torna algo “errado” a partir do momento em que a pessoa aplica errado, seja por falta de conhecimento ou apenas por erro de compreensão do código ou das necessidades que esse projeto exige. Entretanto, sei que existe um cenário em que ela pode ser usada como vantagem, seja para poupar recursos ou redundância.

3-Quais riscos a tabela desnormalizada pode trazer? Como você mitigaria esses riscos?

Uma tabela desnormalizada pode trazer riscos como inconsistência de dados, redundância, maior dificuldade de manutenção e confusão em consultas complexas. Esses problemas ocorrem porque a duplicação de informações aumenta as chances de erros e dificulta a integridade do banco.
Para mitigar esses riscos, é importante iniciar a análise da tabela em um fluxograma, identificando as entidades e suas dependências funcionais. Em seguida, aplicar progressivamente as formas normais (1FN, 2FN, 3FN, 4FN) de acordo com as necessidades do projeto, de modo a reduzir a redundância e garantir maior consistência e confiabilidade nos dados.

4- Qual seria o impacto de não aplicar a BCNF neste cenário?

O principal seria a inconsistência de dados, não íamos conseguir realizar uma pesquisa simples, pois haveria redundância de dados, gerando pesquisas confusas direcionando para 1 ou mais entidades diferentes. Uma sequência de outros erros poderiam acontecer com a falta desse recurso.

